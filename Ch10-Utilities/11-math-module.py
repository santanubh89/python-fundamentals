import math

log_10_base10 = math.log(1000, 10)
print(f'Log 10: {log_10_base10}')

log_10_base_e = math.log(10)
print(f'Ln 10: {log_10_base_e}')

print(f'Pi = {math.pi}, e = {math.e}')

value = 4.35
print(f'floor = {math.floor(value)}, ceil = {math.ceil(value)}')

print(f'cos 45 = {math.cos(45)}')

print(f'Factorial of 6 => {math.factorial(6)}')

from math import pi
radius = 6
print(f'area = {pi * radius ** 2}')

print(f'Hexadecimal number = {hex(12)}')
print(f'Binary number = {bin(12)}')
print(f'Exponential number = {pow(2, 4)}')
print(f'Absolute number = {abs(-12)}')

print(f'Rounded value = {round(math.pi, 1)}')
print(f'Rounded value = {round(math.e, 2)}')