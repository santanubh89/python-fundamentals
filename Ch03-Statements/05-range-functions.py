for num in range(3):
    print(f'Numbers between 0 to 3 = {num}') # 0 1 2

for num in range (5, 10):
    print(f'Numbers between 5 to 10 = {num}') # 5 6 7 8 9

for num in range (1, 10, 3):
    print(f'Numbers between 1 to 10 with steps = {num}') # 1 4 7

for num in range (5, 0, -1):
    print(f'Numbers between 5 to 0 reversed = {num}')